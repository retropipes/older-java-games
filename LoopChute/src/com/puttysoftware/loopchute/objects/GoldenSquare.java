package com.puttysoftware.loopchute.objects;

import com.puttysoftware.loopchute.generic.ColorConstants;
import com.puttysoftware.loopchute.generic.GenericCheckKey;

public class GoldenSquare extends GenericCheckKey {
    // Constructors
    public GoldenSquare() {
        super(ColorConstants.COLOR_YELLOW);
    }

    @Override
    public String getBaseName() {
        return "square";
    }

    @Override
    public String getName() {
        return "Golden Square";
    }

    @Override
    public String getPluralName() {
        return "Golden Squares";
    }

    @Override
    public String getDescription() {
        return "Golden Squares are the keys to Golden Walls.";
    }
}